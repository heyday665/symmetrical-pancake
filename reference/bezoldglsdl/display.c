#include"all.h"
void setRand()
{
	time_t seed;
	seed = time(0);
	srand48(seed);

	setPoints();
}

void setPoints()
{
	int i,j;
	for (i=0;i<4;i++){
		for(j=0;j<4;j++){
			bez[i][j].x = drand48() * 2 -1;
			bez[i][j].y = drand48() * 2 -1;
			bez[i][j].z = drand48() * 2 -1;
		}
	}
}


